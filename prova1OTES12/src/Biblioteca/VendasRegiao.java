package Biblioteca;

public class VendasRegiao {
	String Funcionario;
	String Trimestre;
	String Regiao;
	float Vendas;

	public VendasRegiao (String Funcionario, String Trimestre, String Regiao, float Vendas) {
		this.Funcionario = Funcionario;
		this.Trimestre = Trimestre;
		this.Regiao = Regiao;
		this.Vendas = Vendas;	
	}

}
