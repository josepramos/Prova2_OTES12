package Biblioteca;

public class Cota {
	String Funcionario;
	String Trimestre;
	String Regiao;
	float Cota;	

	public Cota (String Funcionario, String Trimestre, String Regiao, float Cota) {
		this.Funcionario = Funcionario;
		this.Trimestre = Trimestre;
		this.Regiao = Regiao;
		this.Cota = Cota;	
	}

}
