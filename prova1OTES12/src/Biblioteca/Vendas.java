package Biblioteca;

public class Vendas {
	String Funcionario;
	String Trimestre;
	String Cidade;
	float Vendas;

	public Vendas (String Funcionario, String Trimestre, String Cidade, float Vendas) {
		this.Funcionario = Funcionario;
		this.Trimestre = Trimestre;
		this.Cidade = Cidade;
		this.Vendas = Vendas;	
	}

}
